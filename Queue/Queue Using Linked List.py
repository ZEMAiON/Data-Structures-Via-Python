# QUEUE USING LINKED LIST
import sys
class Node:
    def __init__(s,data):
        s.data=data
        s.next=None
        s.prev=None
class Queue:
    def __init__(s):
        s.head=None
    def enqueue(s):
        data=eval(input("Enter data: "))
        if s.head is None:
            s.head=Node(data)
        else:
            temp=s.head
            new_node=Node(data)
            while temp.next is not None:
                temp=temp.next
            new_node.next=None
            new_node.prev=temp
            temp.next=new_node
                
