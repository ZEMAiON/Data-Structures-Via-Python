# QUEUE USING CLASS
import sys
l=[]
class Queue:
    def __init__(s):
        s.front=0
        s.rear=0
        s.max_len=int(input("Size of stack : "))
    def enqueue(s):
        if s.rear!=s.max_len:
            l.append(eval(input("Enter No:")))
            s.rear+=1
        else:
            print("Overflow")
    def dequeue(s):
        if s.front+1<=s.rear:
            print("elememt to be deleted",l[s.front])
            l.pop(s.front)
            l.insert(0,"#")
            s.front+=1
        else:
            print("underflow")
q=Queue()
while True:
    print("1.enqueue operation\n2.dequeue operation\n3.queue view\n4.exit\n")
    choice=int(input("UR choice : "))
    if choice==1:q.enqueue()
    if choice==2:q.dequeue()
    if choice==3:print("QUEUE VIEW",*l)
    if choice==4:print(sys.exit())
    print('\n')
