# STACK USING CLASS
import sys
l=[]
class Stack:
    def __init__(s):
        s.top=0
        s.max_len=input("Size of stack : ")
    def push(s):
        if s.max_len!=s.top:
            l.append(eval(input("Enter No:")))
            s.top+=1
        else:
            print("Overflow")
    def pop(s):
        if s.top!=0:
            print("elememt to be deleted",l[s.top-1])
            l.pop(s.top-1)
            s.top-=1
        else:
            print("underflow")
s=Stack()
while True:
    print("1.push operation\n2.pop operation\n3.top element & size\n4.stack view\n5.exit\n")
    choice=int(input("UR choice : "))
    if choice==1:s.push()
    if choice==2:s.pop()
    if choice==3:print(f"TOP ELEMENT - {l[s.top-1]} & SIZE - {s.top}")
    if choice==4:print("stack view",*l)
    if choice==5:print(sys.exit())
    print('\n')
